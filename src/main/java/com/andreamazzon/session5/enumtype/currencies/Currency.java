package com.andreamazzon.session5.enumtype.currencies;

/**
 * Enum type, grouping the name of five currencies.
 *
 * @author Andrea Mazzon
 *
 */
public enum Currency {

	/*
	 * Any object of type Currency can be represented by one and only one of these
	 * types. They are implicitly public, static, final. Static and final means
	 * constants, and that's why they are capitalized: by a Java convention, static
	 * final fields are capitalized.
	 */
	EURO, DOLLAR, POUND, YEN, YUAN
}
